                                <h3>Hogyan készül el a kiválasztott munka?</h3>
                                <p>
                                    •	Miután a Sprint Célját meghatározta és a Sprintbe kerülő Termék Backlog tételeket kiválasztotta, a Fejlesztőcsapat eldönti, hogy a Sprint során miként építi be ezt a funkcionalitást a “Kész” termék inkrementumba. 
                                    •	Az erre a Sprintre kiválogatott Termék Backlog tételeket, valamint ezek leszállítási tervét együttesen Sprint Backlognak (Sprint Teendőlista) nevezik. 
                                    •	A Fejlesztőcsapat általában a Termék Backlog működő termék inkrementummá konvertálásához szükséges feladatok meghatározásával és a rendszer megtervezésével kezdi meg a munkát. 
                                    •	A munkamennyiség- és becsült ráfordítás szerint is változhat. 
                                    •	Mindamellett a Sprint Tervezés során elegendő mennyiségű kivitelezendő munkát terveznek meg a Fejlesztőcsapat részére annak érdekében, hogy megalapozottan előre tudják vetíteni, hogy mit vélnek elvégezhetőnek a soron következő Sprint alatt. 
                                    •	A megbeszélés végére a Fejlesztőcsapat a Sprint első napjaira tervezett feladatokat gyakran egy napos vagy annál kisebb részekre bontja le. 
                                    •	A Fejlesztőcsapat önszerveződve vállalja el a Sprint Backlogban szereplő egyes feladatokat a Sprint Tervezés alatt, valamint amennyire szükséges, a Sprint közben is. 
                                    •	A Terméktulajdonos segíthet tisztázni a kiválasztott Termék Backlog tételeket és kompromisszumokat köthet. 
                                    •	Ha a Fejlesztőcsapat úgy ítéli meg, hogy túl sok vagy túl kevés az elvégzendő munka, újratárgyalhatja a kiválasztott Termék Backlog tételeket a Terméktulajdonossal. 
                                    •	A Fejlesztőcsapat másokat is meghívhat a megbeszélésre, hogy technikai vagy szakterületi tanácsokat adjanak. 
                                    •	A Sprint Tervezés végére a Fejlesztőcsapatnak el kell tudni magyarázni a Terméktulajdonosnak és a Scrum Mesternek, hogy miként szándékozik önszerveződő csapatként dolgozni a Sprint Cél megvalósítása és az elvárt inkrementum elkészítése érdekében.
