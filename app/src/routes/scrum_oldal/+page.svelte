                                <p>-A Sprintben végzendő munkát a Sprint Tervezésen tervezik meg. 
                                    -Ez a terv a teljes Scrum Csapat közös munkájának eredménye.
                                    -A Sprint Tervezés időtartama egy hónapos Sprint esetében legfeljebb nyolc óra. 
                                    -Rövidebb Sprintek esetén az esemény időtartama általában kisebb. 
                                    -A Scrum Mester biztosítja, hogy az esemény megtörténjen, és a résztvevők megértsék annak célját. A Scrum Mester ráneveli a Scrum Csapatot az időkorlátok betartására.
                                    -A Sprint Tervezés a következő kérdésekre válaszol:
                                    •	Mit fog tartalmazni a következő Sprint eredményeképpen szállítandó Inkrementum?
                                    •	Hogyan lehet elvégezni az Inkrementum előállításához szükséges munkát?
                                    -Első kérdéskör: Mi fog elkészülni ebben a Sprintben?
                                    •	A Fejlesztőcsapat azon dolgozik, hogy felvázolja a Sprint során megvalósítandó funkcionalitást. 
                                    •	A Terméktulajdonos bemutatja a Sprint során elérendő célt és azokat a Termék Backlog tételeket, amelyek megvalósításával a Sprint eléri a Sprint Célt. A teljes Scrum Csapat együttműködik a Sprintben elvégzendő munka megértésének érdekében.
                                    •	Ennek a megbeszélésnek a bemeneti elemei a Termék Backlog, a legutóbbi termék Inkrementum, a Fejlesztőcsapat tervezett kapacitása a Sprint ideje alatt, valamint a Fejlesztőcsapat korábbi teljesítménye.
                                    •	Az, hogy az adott Sprint számára a Termék Backlogból hány tételt választanak ki, egyedül a Fejlesztőcsapaton múlik. Kizárólag a Fejlesztőcsapat tudhatja, hogy mit képes végrehajtani a soron következő Sprintben.
                                    •	Miután a Fejlesztőcsapat előre jelezte, hogy a Termék Backlog mely elemeit fogja leszállítani a Sprint során, a Scrum Csapat elkészíti a Sprint Célt <ide majd kell egy link>. 
