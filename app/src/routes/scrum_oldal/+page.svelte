                                <h3>Sprint Áttekintés:</h3>
                                    <ul>
                                        <li>A Sprint Áttekintést (Sprint Review) a Sprint végén tartják azzal a céllal, hogy ellenőrizzék az Inkrementumot és szükség esetén módosítsák a Termék Backlogot.</li>

                                        <li>A Sprint Áttekintésen a Scrum Csapat tagjai és az megrendelő oldal egyeztetik, hogy mi történt a Sprint során.  </li>

                                        <li>Ezt és a Sprint során a Termék Backlogban történt változásokat alapul véve a résztvevők egyeztetik a következő időszakban végrehajtandó, optimális/maximális értéket képviselő teendőket.  </li>

                                        <li>Ez egy informális megbeszélés, melynek célja nem a projekt státuszának riportolása, hanem hogy az Inkrementum bemutatásán keresztül visszajelzés érkezzen a megrendelő oldal részéről, valamint, hogy erősítse az együttműködést a két oldal között.</li>
                                        <li>Ennek a megbeszélésnek az időtartama egy 1 hónapos sprint esetén 4 órára korlátozódik. Rövidebb Sprintek esetében általában rövidebb ideig tart. </li> 
                                        <li>A Scrum Mester gondoskodik arról, hogy ez az esemény létrejöjjön és a résztvevők megértsék annak célját. A Scrum Mester mindenkit figyelmeztet az időkorlát betartására. </li>
                                        <li>A Sprint Áttekintés az alábbi elemeket tartalmazza: </li>

                                        <ul class="bekezdes">
                                            <li>A résztvevői kör a Scrum Csapatból és a Termék Tulajdonos által meghívott kulcs érintettekből áll; </li>
