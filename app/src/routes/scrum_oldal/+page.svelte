                                <h3>Napi Scrum:</h3>
                                    <ul>
                                        <li>A Napi Scrum megbeszélés egy maximum 15 perc időtartamú megbeszélés, ahol a Fejlesztőcsapat összehangolja a tevékenységeket, és megtervezi az elkövetkezendő 24 órát.</li>
                                        <li>Ezt a legutóbbi Napi Scrum megbeszélés óta elvégzett feladatok elemzésével, majd a következő Napi Scrum előtt elvégezhető feladatok megtervezésével teszi meg. </li>
                                        <li>A Napi Scrumot minden nap ugyanabban az időben, ugyanazon a helyen tartják az egyszerűség és hatékonyság elősegítése miatt.  </li>
                                        <li>A megbeszélés során a Fejlesztőcsapat minden egyes tagja az alábbiakat fejti ki:</li>
                                        <ul class="bekezdes">
                                            <li>Mit sikerült elvégeznem tegnap, ami a Fejlesztőcsapatot segítette a Sprint Cél elérésében? </li>
                                            <li>Mit fogok tenni ma, ami a Fejlesztőcsapatot segíti a Sprint Cél elérésében? </li>
                                            <li>Látok-e akadályozó tényezőt, ami gátol engem vagy a Fejlesztőcsapatot a Sprint Cél elérésében?</li>
                                        </ul>
                                        <li>A Napi Scrum során a Fejlesztőcsapat ellenőrzi a Sprint Célhoz vezető folyamat haladását és azt, hogy a haladás tendenciája miként változik a Sprint Backlogban szereplő munka teljesítése felé. A Napi Scrum maximálja annak a valószínűségét, hogy a Fejlesztőcsapat eléri a Sprint Célt. A Fejlesztőcsapat vagy egyes csapattagok gyakran közvetlenül a Napi Scrum után részletesen megbeszélik a Napi Scrumon felmerült kérdéseket, vagy áttervezik a Sprintben hátralévő munkát. </li>
                                        <li>A Scrum Mester biztosítja azt, hogy a Fejlesztőcsapat tagjai minden nap megtartsák a megbeszélést, de a Fejlesztőcsapat felelős a Napi Scrum levezetéséért. </li>

                                        <li>A Scrum Mester tanítja meg a Fejlesztőcsapatnak, hogy miként tudják a Napi Scrumot a 15 perces időkereten belül megtartani.</li>

                                        <li>A Napi Scrum javítja a kommunikációt, szükségtelenné tesz egyéb megbeszéléseket, azonosítja és így eltávolíthatóvá teszi a fejlesztés útjába kerülő akadályokat, kihangsúlyozza és elősegíti a gyors döntéshozatalt és növeli a Fejlesztőcsapat tudását. </li>
                                        <li>Ez egy, a munka haladásának elemzése és hangolása szempontjából kulcsfontosságú megbeszélés.</li>
                                    </ul>
