                                <h3>A Sprint:</h3>
                                <p>-A Scrum lelke a legfeljebb egy hónapig tartó Sprint, melynek időtartama alatt előáll egy “Kész”, használható és potenciálisan kibocsátható termék.
                                    - A Sprintek hossza legjobb esetben a teljes fejlesztési idő során azonos. Az előző Sprint lezárása után azonnal egy újabb Sprint kezdődik.
                                    -A Sprintek Sprint Tervezésből, Napi Scrumokból, a fejlesztési munkából, a Sprint Áttekintésből és a Sprint Visszatekintésből épülnek föl.
                                    -A Sprint során:
                                    •	Nem történnek olyan változtatások, melyek veszélyeztetik a Sprint Célját;
                                    •	A minőségi célok nem csökkennek; és,
                                    •	A Terméktulajdonos és a Fejlesztőcsapat újra tárgyalhatja és tisztázhatja a Feladatokat (Scope) az időközben szerzett ismeretek alapján.
                                    -Hasonlóan a projektekhez, a Sprintek is valamilyen cél elérését szolgálják. 
                                    -Minden egyes Sprint tartalmaz:
                                    •	egy meghatározást, ami leírja, hogy minek kell megvalósulnia, egy modellt és egy rugalmas tervet, ami 
                                    •	irányt mutat a megvalósításban. A Sprint részének tekintjük továbbá az elvégzett munkát és az eredményül kapott terméket.
                                    --A Sprintek időtartama egy naptári hónapra korlátozott. 
                                    Ha a Sprint hossza túl hosszú, megváltozhat a megvalósítandó dolog specifikációja, emelkedhet a komplexitása és nőhet a kockázat. 
                                    -A Sprintek úgy biztosítják a tervezhetőséget, hogy legalább minden naptári hónapban egyszer ellenőrzik a Sprint Cél felé haladást, és szükség esetén kiigazítják a folyamatot. 
